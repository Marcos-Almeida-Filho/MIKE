    public void readtableregioes() {
        DefaultTableModel model = (DefaultTableModel) tableregioes.getModel();
        model.setNumRows(0);

        RegioesDAO rd = new RegioesDAO();

        for (RegioesBean rb : rd.read()) {
            model.addRow(new Object[]{
                rb.getId(),
                rb.getNome()
            });
        }
    }

