    private void tableregioesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableregioesMouseClicked
        if (evt.getClickCount() == 2) {
            txtid.setText(tableregioes.getValueAt(tableregioes.getSelectedRow(), 0).toString());
            txtnome.setText(tableregioes.getValueAt(tableregioes.getSelectedRow(), 1).toString());
        }
    }//GEN-LAST:event_tableregioesMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtid.getText().equals("")) {
            if (txtnome.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Coloque um nome!");
                txtnome.requestFocus();
            } else {
                RegioesBean rb = new RegioesBean();
                RegioesDAO rd = new RegioesDAO();

                rb.setNome(txtnome.getText());

                rd.create(rb);
                readtableregioes();
                txtnome.setText("");
                txtnome.requestFocus();
            }
        } else {
            RegioesBean rb = new RegioesBean();
            RegioesDAO rd = new RegioesDAO();

            rb.setNome(txtnome.getText());
            rb.setId(Integer.parseInt(txtid.getText()));

            rd.update(rb);
            readtableregioes();
            txtid.setText("");
            txtnome.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = JOptionPane.showConfirmDialog(rootPane, "Quer cadastrar uma nova região?", "Nova região", JOptionPane.YES_NO_OPTION);
        if (i == 0) {
            txtid.setText("");
            txtnome.setText("");
            txtnome.requestFocus();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


