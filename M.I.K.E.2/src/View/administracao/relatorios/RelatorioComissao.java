    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (cbRepresentante.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione um Representante.");
            cbRepresentante.showPopup();
        } else if (dateInicio.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Selecione uma data de início.");
        } else if (dateFinal.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Seleciona uma data final.");
        } else {
            DefaultTableModel model = (DefaultTableModel) tableComissao.getModel();
            model.setNumRows(0);
            
            int idRep = rd.getIdRepresentante(cbRepresentante.getSelectedItem().toString());
            
            total = 0;
            
            clid.readrepresentantexcliente(idRep).forEach(clib -> {
                cd.readtodosPesquisa(clib.getNome(), Dates.CriarDataCurtaDBJDateChooser(dateInicio.getDate()), Dates.CriarDataCurtaDBJDateChooser(dateFinal.getDate())).forEach(cb -> {
                    double comissao = cb.getValorrecebido() * 0.08;
                    total += comissao;
                    model.addRow(new Object[]{
                        cb.getCliente(),
                        cb.getParcela(),
                        Valores.TransformarDoubleDBemDinheiroComLocal(cb.getValorparcela()),
                        Dates.TransformarDataCurtaDoDB(cb.getDatarecebimento()),
                        "R$ " + Valores.TransformarDoubleDBemString(comissao),
                        "R$ " + Valores.TransformarDoubleDBemString(total)
                    });
                });
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed


