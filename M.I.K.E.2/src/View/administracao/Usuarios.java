    public static void readtableusuarios() {
        DefaultTableModel modelo = (DefaultTableModel) tableusuarios.getModel();
        modelo.setNumRows(0);
        UsuariosDAO ud = new UsuariosDAO();

        ud.readtabelausuariosativo().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getCargo(),
                c.getStatus()
            });
        });
    }

    public static void readcbnivel() {
        Connection con = ConnectionFactory.getConnection();

        PreparedStatement stmt = null;

        ResultSet rs = null;
        try {

            stmt = con.prepareStatement("SELECT * FROM grupo_usuarios");
            rs = stmt.executeQuery();

            while (rs.next()) {
                String nome = rs.getString("nome");
                cbnivel.addItem(nome);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro!" + e);
        } finally {
            ConnectionFactory.closeConnection(con, stmt, rs);
        }
    }

