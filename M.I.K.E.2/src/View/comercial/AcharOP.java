    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tableOPs.getModel();
        model.setNumRows(0);

        od.readOPPorMaterial(idMaterial).forEach(ob -> {
            model.addRow(new Object[]{
                ob.getOp(),
                ob.getCliente()
            });
        });
    }

