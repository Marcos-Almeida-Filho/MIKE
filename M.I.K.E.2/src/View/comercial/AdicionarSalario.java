    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtvalor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor colocar um valor primeiro.");
            txtvalor.requestFocus();
        } else if (cbmotivo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selecione um motivo.");
        } else if (cbmotivo.getSelectedIndex() == 4 && txtoutros.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Escreva o motivo primeiro.");
            txtoutros.requestFocus();
        } else {
            DefaultTableModel model = (DefaultTableModel) Usuarios.tablesalario.getModel();
            
            Date d = new Date();
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            String dia = df.format(d);
            
            model.addRow(new Object[]{
                "",
                dia,
                txtvalor.getText(),
                cbmotivo.getSelectedItem().toString()
            });
            
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cbmotivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbmotivoActionPerformed
        switch (cbmotivo.getSelectedItem().toString()) {
            case "Outros":
                txtoutros.setEnabled(true);
                txtoutros.requestFocus();
                break;
            default:
                txtoutros.setEnabled(false);
        }
    }//GEN-LAST:event_cbmotivoActionPerformed

