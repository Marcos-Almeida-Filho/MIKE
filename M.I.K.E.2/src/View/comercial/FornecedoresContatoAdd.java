    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Fazer tablecontatos ficar default para adicionar ou alterar linhas
        DefaultTableModel model = (DefaultTableModel) Fornecedores.tablecontatos.getModel();
        
        //Verificar se lbllinha tem um número já existente ou não
        if (lbllinha.getText().equals("lbllinha")) {
            //Adicionar linha à tablecontatos
            model.addRow(new Object[]{
                "",
                false,
                txtnome.getText(),
                txtcargo.getText(),
                txttel.getText(),
                txtemail.getText()
            });
        } else {
            //Alterar linha na tablecontatos
            model.setValueAt(txtnome.getText(), Integer.parseInt(lbllinha.getText()), 2);
            model.setValueAt(txtcargo.getText(), Integer.parseInt(lbllinha.getText()), 3);
            model.setValueAt(txttel.getText(), Integer.parseInt(lbllinha.getText()), 4);
            model.setValueAt(txtemail.getText(), Integer.parseInt(lbllinha.getText()), 5);
        }
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed


