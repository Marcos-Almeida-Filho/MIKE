    public static void filltablecondicoes() {
        DefaultTableModel model = (DefaultTableModel) tablecondicao.getModel();
        model.setNumRows(0);
        CondicaoDePagamentoDAO cpd = new CondicaoDePagamentoDAO();

        cpd.read().forEach((cpb) -> {
            model.addRow(new Object[]{
                cpb.getId(),
                cpb.getNome()
            });
        });
    }

