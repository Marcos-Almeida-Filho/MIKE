    public static void readinsumos() {
        DefaultTableModel model = (DefaultTableModel) tableinsumos.getModel();
        model.setNumRows(0);

        idao.readPorTipo(tipo).forEach(ib -> {
            model.addRow(new Object[]{
                ib.getCodigo(),
                ib.getDescricao(),
                ib.getUnidade()
            });
        });
    }

