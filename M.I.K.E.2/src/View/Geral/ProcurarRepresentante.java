    public static void filltablerepresentante() {
        DefaultTableModel model = (DefaultTableModel) tablerepresentante.getModel();
        model.setNumRows(0);

        RepresentantesDAO rd = new RepresentantesDAO();

        rd.read().forEach((rb) -> {
            model.addRow(new Object[]{
                rb.getId(),
                rb.getNome()
            });
        });
    }

