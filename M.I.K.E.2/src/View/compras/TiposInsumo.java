    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
        int numtrue = 0;
        int rowcount = tabletipoinsumo.getRowCount();
        for (int i = 0; i < rowcount; i++) {
            if (tabletipoinsumo.getValueAt(i, 1).equals(true)) {
                numtrue++;
            }
        }
        if (numtrue == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum Tipo de Insumo selecionado.");
        } else {
            for (int i = 0; i < rowcount; i++) {
                if (tabletipoinsumo.getValueAt(i, 1).equals(true)) {
                    tib.setId(Integer.parseInt(tabletipoinsumo.getValueAt(i, 0).toString()));
                    
                    //id = ?
                    tid.delete(tib);
                }
            }
        }
    }//GEN-LAST:event_btnexcluirActionPerformed


