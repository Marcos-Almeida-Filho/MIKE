    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = txtnome.getText();
        String responsavel = txtresponsavel.getText();

        String datacriacao = Dates.CriarDataCompletaParaDB();

        if (txtid.getText().equals("")) {//Se o tipo de insumo ainda não existe
            if (txtnome.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Digite um nome.");
                txtnome.requestFocus();
            } else if (txtresponsavel.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Selecione um responsável.");
            } else {
////////////////Criar Tipo de Insumo
                tib.setNome(nome);
                tib.setResponsavel(responsavel);
                tib.setDatacriacao(datacriacao);

                //nome, responsavel, datacriacao
                tid.create(tib);

                //Pegar id
                tid.readcreated(datacriacao).forEach(tib -> {
                    idcriado = tib.getId();
                });

////////////////Criar Observações
                if (tableobs.getRowCount() > 0) {
                    for (int i = 0; i < tableobs.getRowCount(); i++) {
                        tiob.setIdtipoinsumo(idcriado);
                        tiob.setData(tableobs.getValueAt(i, 1).toString());
                        tiob.setFuncionario(tableobs.getValueAt(i, 2).toString());
                        tiob.setObs(tableobs.getValueAt(i, 3).toString());

                        //idtipoinsumo, data, funcionario, obs
                        tiod.create(tiob);
                    }
                }
            }
            JOptionPane.showMessageDialog(null, "Criado com sucesso");
        } else {//Se o tipo de insumo já está cadastrado
////////////////Atualizar Tipo de Insumo
            tib.setNome(nome);
            tib.setResponsavel(responsavel);
            tib.setId(Integer.parseInt(txtid.getText()));

            //nome = ?, responsavel = ? WHERE id = ?
            tid.update(tib);

////////////////Criar Observações
            if (tableobs.getRowCount() > 0) {
                for (int i = 0; i < tableobs.getRowCount(); i++) {
                    if (tableobs.getValueAt(i, 0).equals("")) {
                        tiob.setIdtipoinsumo(idcriado);
                        tiob.setData(tableobs.getValueAt(i, 1).toString());
                        tiob.setFuncionario(tableobs.getValueAt(i, 2).toString());
                        tiob.setObs(tableobs.getValueAt(i, 3).toString());

                        //idtipoinsumo, data, funcionario, obs
                        tiod.create(tiob);
                    }
                }
            }
            JOptionPane.showMessageDialog(null, "Atualizado com sucesso!");
        }
        readTiposInsumos();
        
        zeracampos();
        
        tabtiposinsumo.setSelectedIndex(0);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tabletipoinsumoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabletipoinsumoMouseClicked
        if (evt.getClickCount() == 2) {
            tabtiposinsumo.setSelectedIndex(1);
            txtid.setText(tabletipoinsumo.getValueAt(tabletipoinsumo.getSelectedRow(), 0).toString());

            int id = Integer.parseInt(txtid.getText());

            tid.click(id).forEach(tib -> {
                txtnome.setText(tib.getNome());
                txtresponsavel.setText(tib.getResponsavel());
            });

            DefaultTableModel modelobs = (DefaultTableModel) tableobs.getModel();
            modelobs.setNumRows(0);

            tiod.read(id).forEach(tiob -> {
                modelobs.addRow(new Object[]{
                    tiob.getId(),
                    tiob.getData(),
                    tiob.getFuncionario(),
                    tiob.getObs()
                });
            });
        }
    }//GEN-LAST:event_tabletipoinsumoMouseClicked

