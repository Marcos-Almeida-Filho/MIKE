    public static void readtableinsumos() {
        DefaultTableModel modelinsumos = (DefaultTableModel) tableinsumos.getModel();
        modelinsumos.setRowCount(0);

        idao.read().forEach(ib -> {
            modelinsumos.addRow(new Object[]{
                ib.getId(),
                false,
                ib.getCodigo(),
                ib.getDescricao()
            });
        });
    }

