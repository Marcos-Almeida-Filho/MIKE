    public void lerFerramentas() {
        DefaultTableModel model = (DefaultTableModel) tableFerramentas.getModel();
        model.setNumRows(0);

        fd.readTodos().forEach(fb -> {
            model.addRow(new Object[]{
                fb.getId(),
                false,
                fb.getNome(),
                fb.getDescricao()
            });
        });
    }

    private void lerFerramenta(String nomeFerramenta) {
        fd.readFerramenta(nomeFerramenta).forEach(fb -> {
            txtNome.setText(fb.getNome());
            txtDescricao.setText(fb.getDescricao());
        });

        lerFamilias(nomeFerramenta);

        lerTamanhos(nomeFerramenta);
    }

    public static void lerFamilias(String nomeFerramenta) {
        DefaultTableModel model = (DefaultTableModel) tableFamilias.getModel();
        model.setNumRows(0);

        ffd.read(nomeFerramenta).forEach(ffb -> {
            model.addRow(new Object[]{
                ffb.getId(),
                false,
                ffb.getNome(),
                ffb.getCodigo(),
                ffb.getDescricao()
            });
        });
    }

    private void lerTamanhos(String nomeFerramenta) {
        DefaultTableModel model = (DefaultTableModel) tableTamanhos.getModel();
        model.setNumRows(0);

        ftd.read(nomeFerramenta).forEach(ftb -> {
            model.addRow(new Object[]{
                ftb.getId(),
                false,
                ftb.getCodigo()
            });
        });
    }

