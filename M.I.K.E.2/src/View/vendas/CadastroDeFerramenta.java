    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um nome primeiro.");
            txtNome.requestFocus();
        } else {
            String nomeFerramenta = txtNome.getText();

            if (idFerramenta == 0) {
                try {
                    fd.create(nomeFerramenta, txtDescricao.getText());

                    for (int i = 0; i < tableFamilias.getRowCount(); i++) {
                        ffd.create(nomeFerramenta, tableFamilias.getValueAt(i, 2).toString(), tableFamilias.getValueAt(i, 3).toString(), tableFamilias.getValueAt(i, 4).toString());
                    }

                    for (int i = 0; i < tableTamanhos.getRowCount(); i++) {
                        ftd.create(nomeFerramenta, tableTamanhos.getValueAt(i, 2).toString());
                    }

                    JOptionPane.showMessageDialog(null, "Criado com sucesso.");
                } catch (SQLException e) {
                    String msg = "Erro ao criar Ferramenta.";
                    JOptionPane.showMessageDialog(null, msg + "\n" + e);

                    new Thread() {
                        @Override
                        public void run() {
                            SendEmail.EnviarErro2(msg, e);
                        }
                    }.start();
                }
            } else {
                try {
                    fd.update(nomeFerramenta, txtDescricao.getText(), idFerramenta);

                    for (int i = 0; i < tableFamilias.getRowCount(); i++) {
                        if (tableFamilias.getValueAt(i, 0).equals("")) {
                            ffd.create(nomeFerramenta, tableFamilias.getValueAt(i, 2).toString(), tableFamilias.getValueAt(i, 3).toString(), tableFamilias.getValueAt(i, 4).toString());
                        }
                    }

                    for (int i = 0; i < tableTamanhos.getRowCount(); i++) {
                        if (tableTamanhos.getValueAt(i, 0).equals("")) {
                            ftd.create(nomeFerramenta, tableTamanhos.getValueAt(i, 2).toString());
                        }
                    }

                    JOptionPane.showMessageDialog(null, "Atualizado com sucesso.");
                } catch (SQLException e) {
                    String msg = "Erro ao atualizar Ferramenta.";
                    JOptionPane.showMessageDialog(null, msg + "\n" + e);

                    new Thread() {
                        @Override
                        public void run() {
                            SendEmail.EnviarErro2(msg, e);
                        }
                    }.start();
                }
            }

            lerFerramenta(txtNome.getText());

            lerFerramentas();
            VM1.lerFerramentas();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

