    private void tableMPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMPMouseClicked
        if (evt.getClickCount() == 2) {
            int idMP = Integer.parseInt(tableMP.getValueAt(tableMP.getSelectedRow(), 0).toString());

            NovaMP nmp = new NovaMP();
            Telas.AparecerTela(nmp);
            nmp.txtNome.setText(tableMP.getValueAt(tableMP.getSelectedRow(), 2).toString());
            nmp.txtCodigo.setText(tableMP.getValueAt(tableMP.getSelectedRow(), 3).toString());
            nmp.txtDescricao.setText(tableMP.getValueAt(tableMP.getSelectedRow(), 4).toString());
            
            NovaMP.idMP = idMP;
        }
    }//GEN-LAST:event_tableMPMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int numTrue = 0;

        for (int i = 0; i < tableMP.getRowCount(); i++) {
            if (tableMP.getValueAt(i, 1).equals(true)) {
                numTrue++;
            }
        }

        if (numTrue == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado.");
        } else {
            int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir as Matérias Primas selecionadas?", "Excluir Matéria Prima", JOptionPane.YES_NO_OPTION);

            if (resp == 0) {
                for (int i = 0; i < tableMP.getRowCount(); i++) {
                    if (tableMP.getValueAt(i, 1).equals(true)) {
                        int id = Integer.parseInt(tableMP.getValueAt(i, 0).toString());

                        mpd.delete(id);
                    }
                }
                lerMPs();
                VM1.lerMP();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


