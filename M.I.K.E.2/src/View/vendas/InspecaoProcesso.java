    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double maior = Double.parseDouble(txtMaior.getText());
        double menor = Double.parseDouble(txtMenor.getText());

        if (txtMaior.getText().equals("") || txtMenor.getText().equals("") || cbInstrumento.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Lance todos os dados primeiro.");
        } else if (menor > maior) {
            JOptionPane.showMessageDialog(null, "Medida Menor está maior que Medida Maior");
        } else {
            md.readMedicao(txtMedida.getText()).forEach(mb -> {
                unidade = " " + mb.getUnidade();
            });
            omd.update(idInspecao, txtMaior.getText() + unidade, txtMenor.getText() + unidade, cbInstrumento.getSelectedItem().toString());
            ProcessoOP.readMedicoesDoProcesso(ProcessoOP.idProcesso);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


