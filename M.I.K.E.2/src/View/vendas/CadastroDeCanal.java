    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String canal = JOptionPane.showInputDialog(null, "Qual o tipo de Canal?", "Novo Canal", JOptionPane.YES_NO_OPTION);

            cd.create(canal);

            JOptionPane.showMessageDialog(null, "Criado com sucesso!");

            lerCanais();
            VM1.lerCanais();
        } catch (Exception e) {
            String msg = "Erro.";

            JOptionPane.showMessageDialog(null, msg + "\n" + e);

            new Thread() {
                @Override
                public void run() {
                    SendEmail.EnviarErro2(msg, e);
                }
            }.start();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int numTrue = 0;

        for (int i = 0; i < tableCanais.getRowCount(); i++) {
            if (tableCanais.getValueAt(i, 1).equals(true)) {
                numTrue++;
            }
        }

        if (numTrue == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado.");
        } else {
            int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir os registros selecionados?", "Excluir Canais", JOptionPane.YES_NO_OPTION);

            if (resp == 0) {
                try {
                    for (int i = 0; i < tableCanais.getRowCount(); i++) {
                        if (tableCanais.getValueAt(i, 1).equals(true)) {
                            int id = Integer.parseInt(tableCanais.getValueAt(i, 0).toString());

                            cd.delete(id);
                        }
                    }
                    JOptionPane.showMessageDialog(null, "ExcluÃ­do com sucesso.");

                    lerCanais();
                    VM1.lerCanais();
                } catch (SQLException e) {
                    String msg = "Erro ao excluir Canal.";

                    JOptionPane.showMessageDialog(null, msg + "\n" + e);

                    new Thread() {
                        @Override
                        public void run() {
                            SendEmail.EnviarErro2(msg, e);
                        }
                    }.start();
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


