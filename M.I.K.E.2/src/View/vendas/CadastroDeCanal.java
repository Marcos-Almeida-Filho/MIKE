    private void lerCanais() {
        DefaultTableModel model = (DefaultTableModel) tableCanais.getModel();
        model.setNumRows(0);

        cd.readTodos().forEach(cb -> {
            model.addRow(new Object[]{
                cb.getId(),
                false,
                cb.getNome()
            });
        });
    }

