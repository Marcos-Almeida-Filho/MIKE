    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int numTrue = 0;

        for (int i = 0; i < tableRev.getRowCount(); i++) {
            if (tableRev.getValueAt(i, 1).equals(true)) {
                numTrue++;
            }
        }

        if (numTrue == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado.");
        } else {
            int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir os revestimentos selecionados?", "Excluir Revestimentos", JOptionPane.YES_NO_OPTION);

            if (resp == 0) {
                for (int i = 0; i < tableRev.getRowCount(); i++) {
                    if (tableRev.getValueAt(i, 1).equals(true)) {
                        int id = Integer.parseInt(tableRev.getValueAt(i, 0).toString());
                        rd.delete(id);
                    }
                }

                lerRevestimentos();
                VM1.lerRevestimento();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed


