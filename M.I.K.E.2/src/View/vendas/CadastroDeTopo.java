    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int numTrue = 0;

        for (int i = 0; i < tableTopos.getRowCount(); i++) {
            if (tableTopos.getValueAt(i, 1).equals(true)) {
                numTrue++;
            }
        }

        if (numTrue == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado.");
        } else {
            int resp = JOptionPane.showConfirmDialog(null, "Deseja excluir os Topos selecionados?", "Excluir Topos", JOptionPane.YES_NO_OPTION);

            if (resp == 0) {
                for (int i = 0; i < tableTopos.getRowCount(); i++) {
                    if (tableTopos.getValueAt(i, 1).equals(true)) {
                        int id = Integer.parseInt(tableTopos.getValueAt(i, 0).toString());
                        td.delete(id);
                    }
                }

                lerTopos();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String topo = JOptionPane.showInputDialog(null, "Qual o nome do Topo para cadastrar?", "Cadastrar Topo", JOptionPane.YES_NO_OPTION);

            td.create(topo);

            JOptionPane.showMessageDialog(null, "Criado com sucesso.");

            lerTopos();
            VM1.lerTopos();
        } catch (Exception e) {
            String msg = "Erro.";

            JOptionPane.showMessageDialog(null, msg + "\n" + e);

            new Thread() {
                @Override
                public void run() {
                    SendEmail.EnviarErro2(msg, e);
                }
            }.start();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


