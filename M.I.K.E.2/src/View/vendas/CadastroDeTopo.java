    public void lerTopos() {
        DefaultTableModel model = (DefaultTableModel) tableTopos.getModel();
        model.setNumRows(0);

        td.readTodos().forEach(tb -> {
            model.addRow(new Object[]{
                tb.getId(),
                false,
                tb.getNome()
            });
        });
    }

