    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (txtcliente.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Selecione um cliente.");
            ProcurarCliente pc = new ProcurarCliente(this.getClass().getSimpleName());
            Telas.AparecerTela(pc);
        } else if (txtcodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Digite um código.");
            txtcodigo.requestFocus();
        } else if (txtdesc.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Digite uma descrição.");
            txtdesc.requestFocus();
        } else {
            DefaultTableModel model = (DefaultTableModel) VendasMateriais.tabledesccli.getModel();
            model.addRow(new Object[]{
                "",
                false,
                txtcliente.getText(),
                txtcodigo.getText(),
                txtdesc.getText()
            });
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


