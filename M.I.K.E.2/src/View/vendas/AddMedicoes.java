    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int numTrue = 0;

        for (int i = 0; i < tableMedicoes.getRowCount(); i++) {
            if (tableMedicoes.getValueAt(i, 0).equals(true)) {
                numTrue++;
            }
        }

        if (numTrue == 0) {
            JOptionPane.showMessageDialog(null, "Nenhuma medida selecionada.");
        } else {
            DefaultTableModel model = (DefaultTableModel) ProcessosVendas.tableMedicoes.getModel();

            for (int i = 0; i < tableMedicoes.getRowCount(); i++) {
                if (tableMedicoes.getValueAt(i, 0).equals(true)) {
                    model.addRow(new Object[]{
                        "",
                        false,
                        tableMedicoes.getValueAt(i, 1).toString()
                    });
                }
            }
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


