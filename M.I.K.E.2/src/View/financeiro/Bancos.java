    public static void readtablebancos() {
        DefaultTableModel model = (DefaultTableModel) tablebanco.getModel();
        model.setNumRows(0);
        BancosDAO bd = new BancosDAO();

        for (BancosBean bb : bd.read()) {
            model.addRow(new Object[]{
                false,
                bb.getId(),
                bb.getBanco(),
                bb.getConta()
            });
        }
    }

