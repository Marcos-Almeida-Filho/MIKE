    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (txtid.getText().equals("")) {
            BancosBean bb = new BancosBean();
            BancosDAO bd = new BancosDAO();

            bb.setBanco(txtbanco.getText());
            bb.setConta(txtconta.getText());
            bb.setTipo(cbtipo.getSelectedItem().toString());
            //banco, conta, tipo
            bd.create(bb);
            zeracampos();
            tabbancos.setSelectedIndex(0);
            readtablebancos();
        } else {
            BancosBean bb = new BancosBean();
            BancosDAO bd = new BancosDAO();

            bb.setBanco(txtbanco.getText());
            bb.setConta(txtconta.getText());
            bb.setTipo(cbtipo.getSelectedItem().toString());
            //banco, conta, tipo
            bd.create(bb);
            zeracampos();
            tabbancos.setSelectedIndex(0);
            readtablebancos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tablebancoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablebancoMouseClicked
        if (evt.getClickCount() == 2) {
            tabbancos.setSelectedIndex(1);
            txtid.setText(tablebanco.getValueAt(tablebanco.getSelectedRow(), 1).toString());

            BancosDAO bd = new BancosDAO();

            for (BancosBean bb : bd.click(Integer.parseInt(txtid.getText()))) {
                txtbanco.setText(bb.getBanco());
                txtconta.setText(bb.getConta());
                cbtipo.setSelectedItem(bb.getTipo());
            }
        }
    }//GEN-LAST:event_tablebancoMouseClicked


