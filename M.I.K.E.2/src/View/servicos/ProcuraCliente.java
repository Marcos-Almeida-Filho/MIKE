    private void tableclienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableclienteMouseClicked
        //DAO para procura
        ClientesDAO cd = new ClientesDAO();

        if (evt.getClickCount() == 2) {
            switch (origem) {
                case "ServiçoCotação":
                    CotacaoServico.txtnomecliente.setText(tablecliente.getValueAt(tablecliente.getSelectedRow(), 0).toString());

                    cd.readcondicoes(CotacaoServico.txtnomecliente.getText()).forEach((cb) -> {
                        CotacaoServico.txtcondicao.setText(cb.getCondicaodepagamento());
                        CotacaoServico.txtvendedor.setText(cb.getVendedor());
                        CotacaoServico.txtrepresentante.setText(cb.getRepresentante());
                    });

                    this.dispose();
                    break;
                case "ServiçoPedido":
                    PedidoServico.txtclientepedido.setText(tablecliente.getValueAt(tablecliente.getSelectedRow(), 0).toString());

                    cd.readcondicoes(PedidoServico.txtclientepedido.getText()).forEach((cb) -> {
                        PedidoServico.txtcondicao.setText(cb.getCondicaodepagamento());
                        PedidoServico.txtvendedor.setText(cb.getVendedor());
                        PedidoServico.txtrepresentante.setText(cb.getRepresentante());
                    });

                    this.dispose();
                    break;
                case "VendaCotação":
                    break;
                case "VendaPedido":
                    break;
            }
        }
    }//GEN-LAST:event_tableclienteMouseClicked


