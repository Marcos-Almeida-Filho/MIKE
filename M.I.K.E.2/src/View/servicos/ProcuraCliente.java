    public static void table(JTextField text) {
        ClientesDAO cd = new ClientesDAO();

        DefaultTableModel model = (DefaultTableModel) tablecliente.getModel();
        model.setNumRows(0);

        cd.pesquisanome(text.getText()).forEach((cb) -> {
            model.addRow(new Object[]{
                cb.getNome()
            });
        });
    }

