    private void tablegrupoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablegrupoMouseClicked
        if (evt.getClickCount() == 2) {
            //Chamar tableprocessos em OS
            DefaultTableModel model = (DefaultTableModel) OS.tableprocessos.getModel();
            
            //Excluir processos da OS
            OSProcessosDAO osd = new OSProcessosDAO();
            OSProcessosBean osb = new OSProcessosBean();
            
            int rc = OS.tableprocessos.getRowCount();
            for (int i = 0; i < rc; i++) {
                osb.setId(Integer.parseInt(OS.tableprocessos.getValueAt(i, 1).toString()));
                
                //id = ?
                osd.delete(osb);
            }
            
            //Deixar tableprocessos sem linhas
            model.setNumRows(0);

            //Nome do processo
            String nome = tablegrupo.getValueAt(tablegrupo.getSelectedRow(), 1).toString();

            //Pegar processos e colocar na tableprocessos em OS
            ServicoGrupoDeProcessosDAO d = new ServicoGrupoDeProcessosDAO();

            //Pegar o id do grupo
            int id = 0;
            for (ProcessosServicoBean b : d.readidgrupo(nome)) {
                id = b.getId();
            }

            //Pegar os processos novos e colocar na table
            int row = 0;
            ServicoGrupoDeProcessosItensDAO dd = new ServicoGrupoDeProcessosItensDAO();
            for (ServicoGrupoDeProcessosItensBean bb : dd.read(id)) {
                model.addRow(new Object[]{
                    false,
                    "",
                    bb.getProcesso(),
                    "",
                    "",
                    0,
                    0,
                    "",
                    row,
                    OS.txtinicial.getText()
                });
                row++;
            }

            //Fechar
            dispose();
        }
    }//GEN-LAST:event_tablegrupoMouseClicked


