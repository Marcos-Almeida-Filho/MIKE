    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtid.getText().equals("")) {
            if (txtdesc.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Escolha um item primeiro!");
                txtdesc.requestFocus();
            } else if (txtqtd.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Coloque uma quantidade primeiro!");
                txtqtd.requestFocus();
            } else if (txtvalor.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Coloque um valor unitário primeiro!");
                txtvalor.requestFocus();
            } else {
                DecimalFormat formatter = new DecimalFormat("#,###.00");
                DecimalFormat formatterq = new DecimalFormat("#,###");
                DefaultTableModel model = (DefaultTableModel) PedidoServico.tableitensorcamento.getModel();
                int qtd = Integer.parseInt(txtqtd.getText());
                String v = txtvalor.getText().replace(".", "");
                float valor = Float.parseFloat(v.replace(",", "."));
                float tot = qtd * valor;
                String totf = formatter.format(tot);
                String qtdf = formatterq.format(qtd);

                model.addRow(new Object[]{
                    false,
                    "",
                    txtcodigo.getText(),
                    txtdesc.getText(),
                    qtdf,
                    txtvalor.getText(),
                    totf,
                    Dates.TransformarDataCurtaDoDB(Dates.CriarDataCurtaDBJDateChooser(datePrazo.getDate())),
                    "",
                    ""
                });
                PedidoServico.txtvalorcobranca();
                dispose();
            }
        } else {
            DecimalFormat formatter = new DecimalFormat("#,###.00");
            int qtd = Integer.parseInt(txtqtd.getText());
            String v = txtvalor.getText().replace(".", "");
            float valor = Float.parseFloat(v.replace(",", "."));
            float tot = qtd * valor;
            String totf = formatter.format(tot);
            
            ServicoPedidoItensBean spib = new ServicoPedidoItensBean();

            spib.setCodigo(txtcodigo.getText());
            spib.setDescricao(txtdesc.getText());
            spib.setQtde(txtqtd.getText());
            spib.setValor(txtvalor.getText());
            spib.setTotal(totf);
            spib.setPrazoDate(Dates.CriarDataCurtaDBJDateChooser(datePrazo.getDate()));
            spib.setId(Integer.parseInt(txtid.getText()));
            
            //codigo = ?, descricao = ?, qtde = ?, valor = ?, total = ?, prazoDate = ? WHERE id = ?
            spid.update(spib);

            PedidoServico.readitenscobranca();
            
            dispose();
        }
        PedidoServico.txtvalorcobranca();
    }//GEN-LAST:event_jButton1ActionPerformed

