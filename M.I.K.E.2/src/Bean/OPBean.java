public class OPBean {

    private int id;
    private String op;
    private String dataabertura;
    private String dataprevista;
    private String cliente;
    private String dav;
    private int produto;
    private double qtd;
    private double qtdok;
    private double qtdnaook;
    private String status;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getOp() {
        return op;
    }

    public void setOp(String op) {
        this.op = op;
    }

    public String getDataabertura() {
        return dataabertura;
    }

    public void setDataabertura(String dataabertura) {
        this.dataabertura = dataabertura;
    }

    public String getDataprevista() {
        return dataprevista;
    }

    public void setDataprevista(String dataprevista) {
        this.dataprevista = dataprevista;
    }

    public String getCliente() {
        return cliente;
    }

    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    public String getDav() {
        return dav;
    }

    public void setDav(String dav) {
        this.dav = dav;
    }

    public int getProduto() {
        return produto;
    }

    public void setProduto(int produto) {
        this.produto = produto;
    }

    public double getQtd() {
        return qtd;
    }

    public void setQtd(double qtd) {
        this.qtd = qtd;
    }

    public double getQtdok() {
        return qtdok;
    }

    public void setQtdok(double qtdok) {
        this.qtdok = qtdok;
    }

    public double getQtdnaook() {
        return qtdnaook;
    }

    public void setQtdnaook(double qtdnaook) {
        this.qtdnaook = qtdnaook;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
