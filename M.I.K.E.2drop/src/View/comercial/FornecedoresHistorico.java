    public static void read() {
        //DAO para consulta
        FornecedoresAltDAO fad = new FornecedoresAltDAO();
        
        //Default da table para inserção de linhas
        DefaultTableModel model = (DefaultTableModel) tablehist.getModel();
        model.setNumRows(0);
        
        for (FornecedoresAltBean fab: fad.read(Integer.parseInt(Fornecedores.txtidfornecedor.getText()))) {
            String datadb = fab.getData();
            
            model.addRow(new Object[]{
                Dates.datanormal(datadb),
                fab.getUser(),
                fab.getValor(),
                fab.getValoranterior(),
                fab.getValornovo()
            });
        }
    }

