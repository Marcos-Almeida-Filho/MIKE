    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int nr = tableparcelas.getRowCount();
        if (txtid.getText().equals("")) {
            //Salvar a condição de pagamento
            CondicaoDePagamentoBean cpb = new CondicaoDePagamentoBean();
            CondicaoDePagamentoDAO cpd = new CondicaoDePagamentoDAO();

            cpb.setNome(txtnome.getText());
            cpb.setParcelas(Integer.parseInt(cbparcelas.getSelectedItem().toString()));
            cpd.create(cpb);

            //Pegar id da condição
            for (CondicaoDePagamentoBean cpb2 : cpd.readid(txtnome.getText())) {
                txtid.setText(String.valueOf(cpb2.getId()));
            }

            //Salvar parcelas da condição
            ParcelasBean pb = new ParcelasBean();
            ParcelasDAO pd = new ParcelasDAO();

            for (int i = 0; i < nr; i++) {
                pb.setIdcondicao(Integer.parseInt(txtid.getText()));
                pb.setParcela(Integer.parseInt(tableparcelas.getValueAt(i, 0).toString()));
                pb.setDiaparcela(Integer.parseInt(tableparcelas.getValueAt(i, 1).toString()));
                pd.create(pb);
            }
            zeracampos();
            tabcondicoes.setSelectedIndex(0);
            filltablecondicoes();
        } else {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tablecondicoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablecondicoesMouseClicked
        if (evt.getClickCount() == 2) {
            tabcondicoes.setSelectedIndex(1);
            txtid.setText(tablecondicoes.getValueAt(tablecondicoes.getSelectedRow(), 0).toString());
            lblstatus.setText("b");

            CondicaoDePagamentoDAO cpd = new CondicaoDePagamentoDAO();

            for (CondicaoDePagamentoBean cpb : cpd.clicktable(Integer.parseInt(txtid.getText()))) {
                txtnome.setText(cpb.getNome());
                String parcelas = String.valueOf(cpb.getParcelas());
                cbparcelas.setSelectedItem(parcelas);
            }
            lblstatus.setText("a");

            ParcelasDAO pd = new ParcelasDAO();
            DefaultTableModel model = (DefaultTableModel) tableparcelas.getModel();
            model.setRowCount(0);

            for (ParcelasBean pb : pd.read(Integer.parseInt(txtid.getText()))) {
                model.addRow(new Object[]{
                    pb.getParcela(),
                    pb.getDiaparcela()
                });
            }
        }
    }//GEN-LAST:event_tablecondicoesMouseClicked


