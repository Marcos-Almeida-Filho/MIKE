public class ParcelasBean {

    private int id;
    private int idcondicao;
    private int parcela;
    private int diaparcela;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdcondicao() {
        return idcondicao;
    }

    public void setIdcondicao(int idcondicao) {
        this.idcondicao = idcondicao;
    }

    public int getParcela() {
        return parcela;
    }

    public void setParcela(int parcela) {
        this.parcela = parcela;
    }

    public int getDiaparcela() {
        return diaparcela;
    }

    public void setDiaparcela(int diaparcela) {
        this.diaparcela = diaparcela;
    }

}
