public class OSInspecaoBean {
    
    private int id;
    private String idos;
    private String idprocesso;
    private String medida;
    private String medidamaior;
    private String medidamenor;
    private String funcionario;
    private String instrumento;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIdos() {
        return idos;
    }

    public void setIdos(String idos) {
        this.idos = idos;
    }

    public String getIdprocesso() {
        return idprocesso;
    }

    public void setIdprocesso(String idprocesso) {
        this.idprocesso = idprocesso;
    }

    public String getMedida() {
        return medida;
    }

    public void setMedida(String medida) {
        this.medida = medida;
    }

    public String getMedidamaior() {
        return medidamaior;
    }

    public void setMedidamaior(String medidamaior) {
        this.medidamaior = medidamaior;
    }

    public String getMedidamenor() {
        return medidamenor;
    }

    public void setMedidamenor(String medidamenor) {
        this.medidamenor = medidamenor;
    }

    public String getFuncionario() {
        return funcionario;
    }

    public void setFuncionario(String funcionario) {
        this.funcionario = funcionario;
    }

    public String getInstrumento() {
        return instrumento;
    }

    public void setInstrumento(String instrumento) {
        this.instrumento = instrumento;
    }
    
    
}
